# 若没有传递参数则退出脚本
if [ $# -eq 0 ]
then 
    echo "至少应该给一个参数!"
    exit
fi

# 使用循环读取传递的文件，统计文件中的空行数
for file in "$*";do
sum_files+=$(grep "^$" $file | wc -l)
done

echo "这些文件中的空行数: $sum_files";